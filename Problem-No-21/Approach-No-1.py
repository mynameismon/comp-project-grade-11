'''
| Write a program to read two strings and perform concatenation and comparison operations on it |
|-----------------------------------------------------------------------------------------------|
| We use the input() function to receive input from the user and print() function to print it   |
'''

f1 = input("String 1\n")
f2 = input("String 2\n")

print(f"Concatenation: {f1+f2}")
print(f"Equal: {f1==f2}")
print(f"Not equal: {f1!=f2}")
print(f"Greater than: {f1>f2}")
print(f"Less than: {f1<f2}")
print(f"Greater than or equal: {f1>=f2}")
print(f"Less than or equal: {f1<=f2}")
